using System;

namespace OperadoresRelacionales
{
    class MainClass
    {
        public static void Main(string[] args)
        {
            int numA = 10;
            int numB = 5;

            System.Console.WriteLine(numA < numB);
            System.Console.WriteLine(numA > numB);
            System.Console.WriteLine(numA <= numB);
            System.Console.WriteLine(numA >= numB);
            System.Console.WriteLine(numA == numB);
            System.Console.WriteLine(numA != numB);
        }
    }
}
